import type { SubjectCode } from "./combinations";

export type EligibilityStatus =
  | "ELIGIBLE"
  | "POTENTIALLY_ELIGIBLE"
  | "NOT_ELIGIBLE"
  | "INCOMPLETE_INFORMATION";

export interface OLevelRequirement {
  subject: string; // human name
  minGrade: "A" | "B" | "C" | "D";
}

export interface AdmissionRule {
  requiredSubjects: SubjectCode[]; // must have ALL of these as principal
  alternativeSubjects?: SubjectCode[][]; // OR-groups (at least one from each)
  minPrincipalPasses: number; // count of principals with >= E
  minPoints: number; // sum of top 2 principal grade points
  minSubjectGrade?: Partial<Record<SubjectCode, "A" | "B" | "C" | "D" | "E">>;
  oLevel?: OLevelRequirement[];
}

export interface Programme {
  id: string;
  slug: string;
  name: { en: string; sw: string };
  institutionId: string;
  campusId: string;
  durationYears: number;
  tuition: { amount: number; currency: "TZS"; note: { en: string; sw: string } };
  applicationOpens: string;
  applicationDeadline: string;
  heslbEligible: boolean;
  category: string; // Health, Engineering, Business, Education, Law, ICT, Social Sciences, Agriculture, Humanities, Media
  overview: { en: string; sw: string };
  rule: AdmissionRule;
  applicationUrl: string;
  requiredDocuments: string[];
  modules: { en: string; sw: string }[];
  careerIds: string[];
  source: { title: string; url: string };
  lastVerified: string; // ISO date
  status: "active" | "archived";
}

